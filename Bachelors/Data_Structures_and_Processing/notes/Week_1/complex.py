# Creating a complex number
z = complex(3, 4)  # 3 + 4j

# Accessing real and imaginary parts
real_part = z.real  # 3.0
imaginary_part = z.imag  # 4.0

# Printing the complex number
print(z)  # (3+4j)


# Writing a complex number directly
z = 2 + 3j


# Addition of two complex numbers
result = (1 + 2j) + (3 - 4j)
print(result)  # (4-2j)
