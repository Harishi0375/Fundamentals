#include <stdio.h>

int main()
{
    int first = 4, second = 5;
    int sum,difference,product;
    product = first * second;
    sum = first + second;
    difference = first - second;
    printf("product = %d \n",product);
    printf("sum = %d \n",sum);
    printf("difference = %d \n",difference);
    return 0;
}